<template>
  <Input
    placeholder="Введите данные"
    label="Ваша зарплата в месяц"
    :valid="valid"
    class="tax-deduction__input"
    @input="inputChange"
    @focus="inputFocus"
    @blur="inputBlur"
  />
</template>

<script>
import Input from '../Input.vue'
const addition = ' ₽'
export default {
  data(){
    return { valid: true }
  },
  components: { Input },
  methods: {
    inputChange(e){
      const val = e.target.value
      const pos = e.target.selectionStart
      if(! new RegExp(/^\d+$/).test(val) || val <= 0){
        e.target.value = val.substring(0,pos-1) + val.substring(pos, val.length)
        return
      }
      this.valid = true
    },
    inputFocus(e){
      const val = e.target.value
      if(val === '') return
      if(val.includes(addition)) {
        e.target.value = val.slice(0, -addition.length)
      }
    },
    inputBlur(e){
      const val = e.target.value
      if(val === '') {
        this.valid = false
        return
      }
      e.target.value = val + addition
    }
  }
}
</script>
